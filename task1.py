"""
Даны два неупорядоченных набора целых чисел (может быть, с повторениями). 
Выдать без повторений в порядке возрастания все те числа, которые встречаются в обоих наборах.
Пользователь вводит 2 числа. n - кол-во элементов первого множества. m - кол-во элементов второго множества.
Затем пользователь вводит сами элементы множеств.
"""

n = int(input("Введите количество элементов первого множества: "))
m = int(input("Введите количество элементов второго множества: "))

# Ввод элементов первого множества
set1_input = input("Введите элементы первого множества через пробел: ")
set1_list = set1_input.split()[:n]  # Оставляем только первые n элементов
set1 = set(map(int, set1_list))

# Ввод элементов второго множества
set2_input = input("Введите элементы второго множества через пробел: ")
set2_list = set2_input.split()[:m]  # Оставляем только первые m элементов
set2 = set(map(int, set2_list))

# Находим пересечение множеств и выводим результат
common_numbers = sorted(set1.intersection(set2))
print(common_numbers)